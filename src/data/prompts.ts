// Helper function to get random prompt
export function getRandomPrompt(
  type: 'truth' | 'dare',
  gender: 'male' | 'female',
  level: number,
  completedPrompts: Set<string>
): Prompt | null {
  const availablePrompts = prompts.filter(
    (prompt) =>
      prompt.type === type &&
      prompt.gender === gender &&
      prompt.level === level &&
      !completedPrompts.has(prompt.id)
  );

  if (availablePrompts.length === 0) return null;

  const randomIndex = Math.floor(Math.random() * availablePrompts.length);
  return availablePrompts[randomIndex];
} 